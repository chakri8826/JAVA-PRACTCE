import java.util.*;
import java.lang.*;
class Calc{
	double sum(double a,double b){
		return a+b;
	}
	double sub(double a,double b){
		return a-b;
	}
	double mul(double a,double b){
		return a*b;
	}
	double div(double a,double b){
		return a/b;
	}	
	double modulus(double a,double b){
		return a%b;
	} 
	double power(double a,double b){
		return Math.pow(a,b);
	}

}
