import java.lang.Math;
class CalC{
	double sum(double a,double b){
		return a+b;
	}
	double sub(double a,double b){
		return a-b;
	}	
	double multi(double a,double b){
		return a*b;
	}
	double div(double a,double b){
		return a/b;
	}
	double modulus(double a,double b){
		return a%b;
	}
	double pow(double a,double b){
		return Math.pow(a,b);
	}
}
